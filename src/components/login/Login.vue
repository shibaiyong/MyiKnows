          this.$mAlert("用户名和密码不正确");
        }
      });
    },

    rememberMe() {
      //记住密码
      let userInfoStr = JSON.stringify(this.userInfo);
      let userInfo = JSON.parse(userInfoStr);
      if (userInfo.userName == "" || userInfo.passWord == "") {
        this.showMessageBox({
          title: "",
          cancel: "",
          confirme: "确定",
          content: "用户名和密码不能为空",
          confirmCallback: () => {
            this.isVisible = -1;
          }
        });
        return false;
      }
      userInfo.password =
        sha256(userInfo.password).substr(0, 32) +
        userInfo.password +
        sha256(userInfo.password).substr(32, 32);
      iknowsUtil.setCookie("rlUserInfo", 7, userInfo); //设置cookie
    },

    forgotMe() {
      iknowsUtil.deleteCookie("rlUserInfo");
    },

    forgotPassWord() {
      //忘记密码

      this.showMessageBox({
        title: "请输入注册邮箱",
        cancel: "取消",
        confirme: "确定",
        content: "",
        confirmCallback: () => {
          this.showMessageBox({
            title: "",
            cancel: "",
            confirme: "确定",
            content: "邮件已成功发到你的邮箱",
            confirmCallback: () => {
              this.isVisible = -1;
            }
          });
        },
        cancelCallback: () => {
          this.isVisible = -1;
        }
      });
    },

    showMessageBox(textOptions) {
      //调用弹窗组件的方法,形参的数据类型为对象格式。
      this.isVisible = 1;
      this.textOptions = textOptions;
    },

    changeNumber(keepTime, cycleTime) {
      //数字滚动叠加

      let span = this.numberEnd - this.numberStart;
      let translateTime = cycleTime;
      let changeTime = Math.ceil(translateTime / keepTime);
      let step = Math.ceil(span / changeTime);
      let changeNumberInterval = setInterval(() => {
        this.numberStart = this.numberStart + step;
        if (this.numberStart >= this.numberEnd && span >= 0) {
          clearInterval(changeNumberInterval);
          this.numberStart = this.numberEnd;
        } else if (this.numberStart <= this.numberEnd && span < 0) {
          clearInterval(changeNumberInterval);
          this.numberStart = this.numberEnd;
        }
      }, keepTime);
    },

    formValidation() {
      //用户名支持数字，字母，区分大小写；密码支持数字，字母，区分大小写。
      let userName = this.userInfo.userName;
      let passWord = this.userInfo.password;

      let userReg = /^[a-zA-Z\d]{3,20}$/;
      let passwordReg = /^[a-zA-Z\d]{6,20}$/;

      let userReg3 = /^[a-zA-Z\d]{0,2}$/;
      let passwordReg6 = /^[a-zA-Z\d]{0,5}$/;

      let userReg20 = /^[a-zA-Z\d]{20,}$/;
      let passwordReg20 = /^[a-zA-Z\d]{20,}$/;

      this.verifyUserName = userReg.test(userName) ? false : true;
      this.verifyPassWord = passwordReg.test(passWord) ? false : true;

      if (userReg3.test(userName)) {
        this.errorUserNameMessage = "用户名少于3个字符";
        this.errorUserNameShow = true;
      } else if (userReg20.test(userName)) {
        this.errorUserNameMessage = "用户名超出20个字符";
        this.errorUserNameShow = true;
      } else {
        this.errorUserNameShow = false;
      }
      if (passwordReg6.test(passWord)) {
        this.errorPassWordMessage = "密码少于6个字符";
        this.errorPassWordShow = true;
      } else if (passwordReg20.test(passWord)) {
        this.errorPassWordMessage = "密码超出20个字符";
        this.errorPassWordShow = true;
      } else {
        this.errorPassWordShow = false;
      }

      return !this.verifyUserName && !this.verifyPassWord;
    },

    getTotalData() {
      this.totalTimer = setInterval(() => {
        getTotalData().then(res => {
          if (res.data) {
            this.numberEnd = res.data.total;
          }
        });
      }, 10000);
    }
  },
  beforeDestroy(){
    clearInterval( this.totalTimer )
  },
  watch: {
    dataArr(val) {
      if (val.length) {
        this.rememberMe();
      } else {
        this.forgotMe();
      }
    },
    numberEnd() {
      this.changeNumber(5, 1500);
    }
  },
  components: {
    messageBox,
    CheckBox
  }
};
</script>

